public class Main {
    public static void solution(int array[]){
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
    public static void main(String[] args) {
        System.out.println("Hello world!");
        int myArray[] = {1, 5, 2};
        solution(myArray);
    }
}